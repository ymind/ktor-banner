    /**
     * Loads a [FigFont] from a resource name.
     *
     * @param fontName The name of the resource from which to load a [FigFont].
     * @return The [FigFont] loaded from the requested resource.
     * @throws IOException if there is problem loading a [FigFont] from the specified
     * resource.
     */
    @Throws(IOException::class)
    fun loadFigFontResource(fontName: String?): FigFont {
        val resourceName = "fonts/$fontName"
        val stream = FigFonts::class.java.classLoader.getResourceAsStream(resourceName)
            ?: throw IOException("$resourceName not found.")

        return stream.let { FigFont.loadFigFont(it) }
    }
